namespace SimpleSpritePacker
{
    public class SpaceRect
    {
        public int X;
        public int Y;
        public int Width;
        public int Height;

        // Constructors

        public SpaceRect() { }

        public SpaceRect(int x, int y, int width, int height)
        {
            X = x;
            Y = y;
            Width = width;
            Height = height;
        }
    }
}